package com.ease.algorithm.model;

public class SearchModel {
	Long length;
	Long drop;
	String path;
	
	/**
	 * @return the length
	 */
	public Long getLength() {
		return length;
	}
	/**
	 * @param length the length to set
	 */
	public void setLength(Long length) {
		this.length = length;
	}
	/**
	 * @return the drop
	 */
	public Long getDrop() {
		return drop;
	}
	/**
	 * @param drop the drop to set
	 */
	public void setDrop(Long drop) {
		this.drop = drop;
	}
	/**
	 * @return the path
	 */
	public String getPath() {
		return path;
	}
	/**
	 * @param path the path to set
	 */
	public void setPath(String path) {
		this.path = path;
	}
}
